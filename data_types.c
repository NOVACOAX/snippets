#include <stdio.h>

int main(void)
{
    //* Integers (int)
    int int_value = 3;
    printf("Integer : %i\n", int_value);

    //* Characters (char)
    char char_value = 'c';
    printf("Character : %c\n", char_value);

    //* Float (real)
    float float_value = 10.54;
    printf("Float : %.2f\n", float_value);

    //*  Double (real)
    double double_value = 15.50;
    printf("Double : %f\n", double_value);
}